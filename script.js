const trustGrid = document.getElementById("trustGrid");
const methodList = document.getElementById("methodList");
const servicesGrid = document.getElementById("servicesGrid");
const certCards = document.getElementById("certCards");
const galleryGrid = document.getElementById("galleryGrid");
const faqList = document.getElementById("faqList");
const certTabs = document.getElementById("certTabs");
const langToggle = document.getElementById("langToggle");
const menuToggle = document.getElementById("menuToggle");
const siteNav = document.getElementById("siteNav");
const copyright = document.getElementById("copyright");
const lightbox = document.getElementById("lightbox");
const lightboxClose = document.getElementById("lightboxClose");
const lightboxImage = document.getElementById("lightboxImage");
const lightboxTitle = document.getElementById("lightboxTitle");
const lightboxText = document.getElementById("lightboxText");

let currentLang = "ar";
let currentCertFilter = "academic";

function setStaticText(lang) {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");
  document.title = content[lang].title;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const value = content[lang][key];
    if (typeof value === "string") {
      el.textContent = value;
    }
  });

  copyright.textContent = content[lang].copyright(new Date().getFullYear());
}

function renderTrust(lang) {
  trustGrid.innerHTML = content[lang].trust.map(item => `
    <article class="trust-item">
      <strong>${item.title}</strong>
      <span>${item.text}</span>
    </article>
  `).join("");
}

function renderMethods(lang) {
  methodList.innerHTML = content[lang].methods.map(item => `
    <article class="method-card">
      <h4>${item.title}</h4>
      <p>${item.text}</p>
    </article>
  `).join("");
}

function renderServices(lang) {
  servicesGrid.innerHTML = content[lang].services.map(item => `
    <article class="service-card">
      <div class="service-icon">${item.icon || "🩺"}</div>
      <h3>${item.title}</h3>
      <p>${item.text}</p>
    </article>
  `).join("");
}

function createInteractiveCard(item, className) {
  return `
    <article class="${className} has-media" data-open-image="${item.image}" data-open-title="${item.title}" data-open-text="${item.text}" tabindex="0" role="button">
      <div class="card-media">
        <img src="${item.image}" alt="${item.title}" loading="lazy" />
        <span class="zoom-chip">${content[currentLang].clickToZoom}</span>
      </div>
      <h3>${item.title}</h3>
      <p>${item.text}</p>
      <span class="cert-meta">${item.meta || content[currentLang].clickToZoom}</span>
    </article>
  `;
}

function renderCertificates(lang, filter) {
  const items = content[lang].certificates[filter] || [];
  certCards.innerHTML = items.map(item => {
    if (item.image) {
      return createInteractiveCard(item, "cert-card");
    }

    return `
      <article class="cert-card">
        <h3>${item.title}</h3>
        <p>${item.text}</p>
        <span class="cert-meta">${item.meta}</span>
      </article>
    `;
  }).join("");
}

function renderGallery(lang) {
  galleryGrid.innerHTML = content[lang].gallery.map(item => `
    <article class="gallery-card has-media ${item.featured ? "featured" : ""}" data-open-image="${item.image}" data-open-title="${item.title}" data-open-text="${item.text}" tabindex="0" role="button">
      <div class="gallery-media">
        <img src="${item.image}" alt="${item.title}" loading="lazy" />
        <span class="zoom-chip">${content[lang].clickToZoom}</span>
      </div>
      <h3>${item.title}</h3>
      <p>${item.text}</p>
    </article>
  `).join("");
}

function renderFaq(lang) {
  faqList.innerHTML = content[lang].faq.map((item, index) => `
    <details class="faq-item" ${index === 0 ? "open" : ""}>
      <summary class="faq-question">${item.q}</summary>
      <div class="faq-answer">${item.a}</div>
    </details>
  `).join("");
}

function renderAll() {
  setStaticText(currentLang);
  renderTrust(currentLang);
  renderMethods(currentLang);
  renderServices(currentLang);
  renderCertificates(currentLang, currentCertFilter);
  renderGallery(currentLang);
  renderFaq(currentLang);
  Array.from(certTabs.querySelectorAll("button")).forEach(btn => {
    btn.classList.toggle("active", btn.dataset.certFilter === currentCertFilter);
  });
}

function openLightbox({ image, title, text }) {
  lightboxImage.src = image;
  lightboxImage.alt = title;
  lightboxTitle.textContent = title;
  lightboxText.textContent = text;
  lightbox.classList.add("open");
  lightbox.setAttribute("aria-hidden", "false");
  document.body.classList.add("is-locked");
}

function closeLightbox() {
  lightbox.classList.remove("open");
  lightbox.setAttribute("aria-hidden", "true");
  lightboxImage.src = "";
  document.body.classList.remove("is-locked");
}

document.addEventListener("click", (event) => {
  const card = event.target.closest("[data-open-image]");
  if (card) {
    openLightbox({
      image: card.dataset.openImage,
      title: card.dataset.openTitle,
      text: card.dataset.openText
    });
    return;
  }

  if (event.target === lightbox || event.target === lightboxClose) {
    closeLightbox();
  }
});

document.addEventListener("keydown", (event) => {
  const card = event.target.closest?.("[data-open-image]");
  if (card && (event.key === "Enter" || event.key === " ")) {
    event.preventDefault();
    openLightbox({
      image: card.dataset.openImage,
      title: card.dataset.openTitle,
      text: card.dataset.openText
    });
  }

  if (event.key === "Escape" && lightbox.classList.contains("open")) {
    closeLightbox();
  }
});

langToggle.addEventListener("click", () => {
  currentLang = currentLang === "ar" ? "en" : "ar";
  renderAll();
});

certTabs.addEventListener("click", (event) => {
  const button = event.target.closest("button[data-cert-filter]");
  if (!button) return;
  currentCertFilter = button.dataset.certFilter;
  renderCertificates(currentLang, currentCertFilter);
  Array.from(certTabs.querySelectorAll("button")).forEach(btn => {
    btn.classList.toggle("active", btn === button);
  });
});

menuToggle.addEventListener("click", () => {
  const isOpen = siteNav.classList.toggle("open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
  document.body.classList.toggle("is-locked", isOpen);
});

siteNav.addEventListener("click", (event) => {
  if (event.target.tagName === "A" && siteNav.classList.contains("open")) {
    siteNav.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
    document.body.classList.remove("is-locked");
  }
});

lightboxClose.addEventListener("click", closeLightbox);

window.addEventListener("resize", () => {
  if (window.innerWidth > 900) {
    siteNav.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
    document.body.classList.remove("is-locked");
  }
});

renderAll();
