certTabs.addEventListener("click", (event) => {
  const button = event.target.closest("button[data-cert-filter]");
  if (!button) return;
  currentCertFilter = button.dataset.certFilter;
  renderCertificates(currentLang, currentCertFilter);
  Array.from(certTabs.querySelectorAll("button")).forEach(btn => {
    btn.classList.toggle("active", btn === button);
  });
});

menuToggle.addEventListener("click", () => {
  const isOpen = siteNav.classList.toggle("open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
  document.body.classList.toggle("is-locked", isOpen);
});

siteNav.addEventListener("click", (event) => {
  if (event.target.tagName === "A" && siteNav.classList.contains("open")) {
    siteNav.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
    document.body.classList.remove("is-locked");
  }
});

window.addEventListener("resize", () => {
  if (window.innerWidth > 900) {
    siteNav.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
    document.body.classList.remove("is-locked");
  }
});

