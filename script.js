function setStaticText(lang) {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");
  document.title = content[lang].title;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const value = content[lang][key];
    if (typeof value === "string") {
      el.textContent = value;
    }
  });

  copyright.textContent = content[lang].copyright(new Date().getFullYear());
}

function renderTrust(lang) {
  trustGrid.innerHTML = content[lang].trust.map(item => `
    <article class="trust-item">
      <strong>${item.title}</strong>
      <span>${item.text}</span>
    </article>
  `).join("");
}

function renderMethods(lang) {
  methodList.innerHTML = content[lang].methods.map(item => `
    <article class="method-card">
      <h4>${item.title}</h4>
      <p>${item.text}</p>
    </article>
  `).join("");
}

function renderServices(lang) {
  servicesGrid.innerHTML = content[lang].services.map(item => `
    <article class="service-card">
      <h3>${item.title}</h3>
      <p>${item.text}</p>
    </article>
  `).join("");
}

function renderCertificates(lang, filter) {
  const items = content[lang].certificates[filter] || [];
  certCards.innerHTML = items.map(item => `
    <article class="cert-card">
      <h3>${item.title}</h3>
      <p>${item.text}</p>
      <span class="cert-meta">${item.meta}</span>
    </article>
  `).join("");
}

function renderGallery(lang) {
  galleryGrid.innerHTML = content[lang].gallery.map(item => `
    <article class="gallery-card ${item.featured ? "featured" : ""}">
      <div class="gallery-media">
        ${item.image
          ? `<img src="${item.image}" alt="${item.title}" />`
          : `<div class="gallery-placeholder">${lang === "ar" ? "سيتم إضافة الصورة لاحقاً" : "Image will be added later"}</div>`}
      </div>
      <h3>${item.title}</h3>
      <p>${item.text}</p>
    </article>
  `).join("");
}

function renderFaq(lang) {
  faqList.innerHTML = content[lang].faq.map((item, index) => `
    <details class="faq-item" ${index === 0 ? "open" : ""}>
      <summary class="faq-question">${item.q}</summary>
      <div class="faq-answer">${item.a}</div>
    </details>
  `).join("");
}

function renderAll() {
  setStaticText(currentLang);
  renderTrust(currentLang);
  renderMethods(currentLang);
  renderServices(currentLang);
  renderCertificates(currentLang, currentCertFilter);
  renderGallery(currentLang);
  renderFaq(currentLang);
  Array.from(certTabs.querySelectorAll("button")).forEach(btn => {
    btn.classList.toggle("active", btn.dataset.certFilter === currentCertFilter);
  });
}

